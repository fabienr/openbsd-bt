#define FEATUREBITMASK_MAX_BYTE 17
const char *
btfeaturebitmask(uint8_t byte, uint8_t bit)
{
	struct bt_featurebitmask_str *bitmask;
	int i;
	if (byte > FEATUREBITMASK_MAX_BYTE)
		return ("Reserved for future use");
	if (bit >= 8)
		return ("invalid command bitmask bit");
	for (i=0; i < nitems(bt_featurebitmask_strs); i++) {
		bitmask = &bt_featurebitmask_strs[i];
		if (bitmask->byte == byte && bitmask->bit == bit)
			return (bitmask->desc);
		else if (bitmask->byte > byte)
			break;
	}
	return ("Reserved for future use");
}
