const char *
btstrerror(int err)
{
	if (err < (1 << BT_ERR_SHIFT))
		return (strerror(err));
	err >>= BT_ERR_SHIFT;
	if (err >= nitems(bt_error_strs))
		return ("invalid error code");
	return (bt_error_strs[err]);
}

